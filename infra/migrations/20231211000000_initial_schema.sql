CREATE INDEX idx_websites_tenant_id ON websites(tenant_id);
CREATE INDEX idx_websites_slug ON websites(slug);
CREATE INDEX idx_websites_status ON websites(status);
CREATE INDEX idx_websites_preset_id ON websites(preset_id);
CREATE INDEX idx_websites_tenant_status ON websites(tenant_id, status);

-- Website data indexes
CREATE INDEX idx_website_data_website_id ON website_data(website_id);

-- Website sections indexes
CREATE INDEX idx_website_sections_website_id ON website_sections(website_id);
CREATE INDEX idx_website_sections_order ON website_sections(website_id, "order");

-- Website modules indexes
CREATE INDEX idx_website_modules_website_id ON website_modules(website_id);
CREATE INDEX idx_website_modules_module_id ON website_modules(module_id);

-- Tenant modules indexes
CREATE INDEX idx_tenant_modules_tenant_id ON tenant_modules(tenant_id);
CREATE INDEX idx_tenant_modules_module_id ON tenant_modules(module_id);

-- Presets indexes
CREATE INDEX idx_presets_enabled ON presets(enabled);
CREATE INDEX idx_presets_category ON presets(category);

-- Events indexes
CREATE INDEX idx_events_tenant_id ON events(tenant_id);
CREATE INDEX idx_events_processed_at ON events(processed_at);
CREATE INDEX idx_events_event_type ON events(event_type);
CREATE INDEX idx_events_retry ON events(retry_count, failed_at) WHERE processed_at IS NULL AND failed_at IS NOT NULL;
CREATE INDEX idx_events_unprocessed ON events(created_at) WHERE processed_at IS NULL;
CREATE INDEX idx_events_created_at ON events(created_at DESC);
CREATE INDEX idx_events_tenant_type_time ON events(tenant_id, event_type, created_at DESC);

-- Module configs indexes
CREATE INDEX idx_module_configs_tenant_id ON module_configs(tenant_id);

-- Files indexes
CREATE INDEX idx_files_user_id ON files(user_id);
CREATE INDEX idx_files_created_at ON files(created_at);

-- File operations audit indexes
CREATE INDEX idx_file_operations_audit_user_id ON file_operations_audit(user_id);
CREATE INDEX idx_file_operations_audit_created_at ON file_operations_audit(created_at);

-- ============================================================================
-- TRIGGERS
-- ============================================================================

-- Update timestamp trigger for user_storage_quota
CREATE OR REPLACE FUNCTION update_user_storage_quota_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_user_storage_quota_updated_at
BEFORE UPDATE ON user_storage_quota
FOR EACH ROW
EXECUTE FUNCTION update_user_storage_quota_timestamp();

