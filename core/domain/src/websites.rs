use serde::{Deserialize, Serialize};
use uuid::Uuid;
use chrono::{DateTime, Utc};

/// Website creation mode
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum CreationMode {
    /// Created from scratch by the user
    FromScratch,
    /// Created from a preset template
    FromPreset,
}

impl Default for CreationMode {
    fn default() -> Self {
        Self::FromScratch
    }
}

/// Website status enum
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "lowercase")]
pub enum WebsiteStatus {
    Draft,
    Published,
}

impl Default for WebsiteStatus {
    fn default() -> Self {
        Self::Draft
    }
}

/// Website represents an account's website that can combine multiple modules
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Website {
    pub id: Uuid,
    pub account_id: Uuid,
    pub slug: String,
    pub title: String,
    pub tagline: String,
    pub status: WebsiteStatus,
    pub creation_mode: CreationMode,
    pub preset_id: Option<Uuid>,
    pub metadata: serde_json::Value,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
}

/// WebsiteData contains the content generated by modules
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WebsiteData {
    pub website_id: Uuid,
    pub data: serde_json::Value,
    pub updated_at: DateTime<Utc>,
}

impl WebsiteData {
    pub fn new(website_id: Uuid) -> Self {
        Self {
            website_id,
            data: serde_json::json!({}),
            updated_at: Utc::now(),
        }
    }
}

/// Extension represents an available extension in the system catalog
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Extension {
    pub id: Uuid,
    pub name: String,
    pub slug: String,
    pub version: String,
    pub description: String,
    pub category: String,
    pub enabled: bool,
    pub default_settings: serde_json::Value,
    pub created_at: DateTime<Utc>,
}

/// WebsiteExtension represents an activated extension for a specific website
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WebsiteExtension {
    pub id: Uuid,
    pub website_id: Uuid,
    pub extension_id: Uuid,
    pub settings: serde_json::Value,
    pub enabled: bool,
    pub activated_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
}

/// Section type for website content blocks
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum SectionType {
    Hero,
    About,
    Projects,
    Skills,
    Experience,
    Education,
    Contact,
    Blog,
    Gallery,
    Testimonials,
    Services,
    Pricing,
    Faq,
    Custom,
}

impl Default for SectionType {
    fn default() -> Self {
        Self::Custom
    }
}

/// Section layout options
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum SectionLayout {
    Full,
    Split,
    Grid,
    List,
    Cards,
    Timeline,
    Custom,
}

impl Default for SectionLayout {
    fn default() -> Self {
        Self::Full
    }
}

/// WebsiteSection represents a content block within a website
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WebsiteSection {
    pub id: Uuid,
    pub website_id: Uuid,
    pub module_id: Option<Uuid>,
    pub section_type: SectionType,
    pub slug: String,
    pub title: String,
    pub order: i32,
    pub layout: SectionLayout,
    pub settings: serde_json::Value,
    pub data: serde_json::Value,
    pub visible: bool,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
}

/// Preset configuration for auto-setup
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PresetConfig {
    pub modules: Vec<String>,
    pub sections: Vec<PresetSectionConfig>,
    pub default_settings: serde_json::Value,
}

/// Section configuration within a preset
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct PresetSectionConfig {
    pub section_type: SectionType,
    pub slug: String,
    pub title: String,
    pub order: i32,
    pub layout: SectionLayout,
    pub settings: serde_json::Value,
}

/// Preset represents a template for quick website creation
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Preset {
    pub id: Uuid,
    pub name: String,
    pub slug: String,
    pub description: String,
    pub category: String,
    pub config: PresetConfig,
    pub thumbnail_url: Option<String>,
    pub enabled: bool,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
}

