    #[test]
    fn test_website_creation() {
        let id = Uuid::new_v4();
        let account_id = Uuid::new_v4();
        let slug = "my-website".to_string();
        let title = "My Website".to_string();
        let tagline = "A great website".to_string();

        let website = Website {
            id,
            account_id,
            slug: slug.clone(),
            title: title.clone(),
            tagline: tagline.clone(),
            status: WebsiteStatus::Draft,
            creation_mode: CreationMode::FromScratch,
            preset_id: None,
            metadata: serde_json::json!({}),
            created_at: Utc::now(),
            updated_at: Utc::now(),
        };

        assert_eq!(website.id, id);
        assert_eq!(website.slug, slug);
        assert_eq!(website.title, title);
        assert_eq!(website.tagline, tagline);
        assert_eq!(website.status, WebsiteStatus::Draft);
        assert_eq!(website.creation_mode, CreationMode::FromScratch);
        assert!(website.preset_id.is_none());
    }

